def get_region_choices():
    return [("", "Selecciona región")] + [
        (region, REGION_LABELS.get(region, region)) for region in REGION_CITY_COMMUNE_MAP.keys()
    ]


def get_city_choices(region):
    if not region:
        return [("", "Selecciona ciudad")]
    return [("", "Selecciona ciudad")] + [(city, city) for city in REGION_CITY_COMMUNE_MAP.get(region, {}).keys()]


def get_commune_choices(region, city):
    if not region or not city:
        return [("", "Selecciona comuna")]
    communes = REGION_CITY_COMMUNE_MAP.get(region, {}).get(city, [])
    return [("", "Selecciona comuna")] + [(commune, commune) for commune in communes]
